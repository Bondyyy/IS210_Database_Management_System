-- 1. Bảng danh mục lỗi
CREATE TABLE LOI (
    MALOI VARCHAR2(10) PRIMARY KEY,
    NOIDUNG NVARCHAR2(255) NOT NULL,
    MUCTIENPHAT NUMBER(15, 2)
);

-- 2. Bảng đối tượng vi phạm
CREATE TABLE DOITUONG (
    MADT VARCHAR2(10) PRIMARY KEY,
    HOTEN NVARCHAR2(100) NOT NULL,
    CMND VARCHAR2(12) UNIQUE,
    DIACHI NVARCHAR2(255),
    BIENKS VARCHAR2(15),
    TONGTIENPHAT NUMBER(15, 2) DEFAULT 0
);

-- 3. Bảng chi tiết vi phạm
CREATE TABLE VIPHAM (
    MALOI VARCHAR2(10),
    MADT VARCHAR2(10),
    THOIDIEMVP DATE,
    NGAYHEN DATE,
    CONSTRAINT PK_VIPHAM PRIMARY KEY (MALOI, MADT, THOIDIEMVP),
    CONSTRAINT FK_LOI FOREIGN KEY (MALOI) REFERENCES LOI(MALOI),
    CONSTRAINT FK_DOITUONG FOREIGN KEY (MADT) REFERENCES DOITUONG(MADT)
);
